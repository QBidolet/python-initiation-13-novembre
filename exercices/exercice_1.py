"""
Exercice 1 : Manipulation de Variables et Expressions
Créer un script qui demande à l'utilisateur son nom,
son âge, et sa ville de résidence,
puis affiche ces informations dans une phrase complète.
Exemple :
Bonjour, je m'appelle Quentin, j'ai 29 ans et je vis à Paris.
"""

nom = input("Quel est votre nom ?\n")
age = input("Quel est votre âge ?\n")
ville = input("Où habitez-vous ?\n")
print(f"Bonjour, je m'appelle {nom}, j'ai {age} ans et je vis à {ville}.")